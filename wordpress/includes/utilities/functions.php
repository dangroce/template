if(!function_exists('get_exclusive_id')){
	function get_exclusive_id($template){
		$template = 'templates/'.$template.'.exc.php';
		$pages = get_posts(array(
			'meta_key' => '_wp_page_template',
			'meta_value' => $template,
			'posts_per_page' => 1,
			'post_type' => 'page',
		));

		if (!empty($pages)) {
			return $pages[0]->ID;
		}
	}
}

